1. ログ(意味、確認の仕方)
コンピューターに記録される情報のこと
確認の仕方はログファイルをみる
ブラウザでWebサイトにアクセスすると、サーバー上で稼働しているApacheがHTMLコンテンツを
ブラウザに返しますが、それと同時にアクセス記録を残します。
その記録が残されるファイルがログファイルです。

2. クラウドサーバー、レンタルサーバー、VPSの違い
クラウドサーバー：複数の仮想サーバーを個別で利用
レンタルサーバー：１つの物理サーバーを複数ユーザーで共用
VPS：１つの仮想サーバー群を個別で利用

3. PHPのFWの種類と特徴
フレームワークは有名なものだけでも10種類以上ある
・CakePHP
フレームワークの性能自体はそこまで高くなく、重いと言われがちですが、
機能が豊富で開発がしやすいことと、日本語ドキュメントやコミュニティが非常に充実していること

・Zend Framework
豊富なライブラリを含んでいる
PHPフレームワークのお手本のようなもの
性能自体はCakePHPよりは優れていますが、機能が豊富であるため、最近流行している軽量フレームワークには及ばない
日本語のドキュメントやコミュニティの充実度は若干見劣りしてしまう

・Symfony
高速に動作することが利点で、PHPのフレームワークを使う際に速度を重視するような場合に使われることが多い
ラグインシステムが優秀なため、比較的機能追加をしやすくなっている点も人気
日本ではそこそこの利用度ですが、海外ではかなりのシステムで使用されている

・Code Igniter
機能は少なめですが、軽量で高速に動作することが売り
構成も単純でわかりやすいため、フレームワークの学習用に最適
比較的小規模で、速度が要求されるシステムに使用されることが多い
一時期ライセンスの問題があった関係から、利用者がLaravelなどに移ってしまったため、利用者は多くない

・Fuel PHP
Code Igniterと同様に軽量かつ高速で、単純なため、学習用に最適なフレームワーク

・Ethna
GREEの藤本真樹氏によって開発されたフレームワークですが、現在はオープンソースになっている
システムを効率よく開発することをコンセプトとしているため、
細かい処理についてはフレームワーク側がサポートしてくれる
日本の開発者が開発したことから日本語のドキュメントも充実している
面倒な処理をフレームワークが請け負ってくれることはバグの発生を抑える上で非常に有効なことですので、
その点から見てもEthnaを使って見る価値はある

・Laravel
Code IgniterやFuelPHPと同じ系統のフレームワークとなっている
作られたのも非常に最近のため、わかりやすさ、書きやすさだけでなく、コード自体も非常にきれいに書かれている
サードパーティーで作られているライブラリも非常に優秀なものが多い





